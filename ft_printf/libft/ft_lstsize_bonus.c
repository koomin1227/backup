#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	t_list	*p;
	int		cnt;

	cnt = 0;
	p = lst;
	while (p != 0)
	{
		cnt += 1;
		p = p->next;
	}
	return (cnt);
}
